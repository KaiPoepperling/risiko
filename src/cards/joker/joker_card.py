class JokerCard(object):
    """
    """
    def __init__(self):
        """
        """
        self._name = 'joker'
        self._continent = None
        self._unit = 'joker'

    @property
    def name(self):
        """
        """
        return self._name

    @property
    def continent(self):
        """
        """
        return self._continent

    @property
    def unit(self):
        """
        """
        return self._unit
